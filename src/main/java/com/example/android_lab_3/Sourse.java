package com.example.android_lab_3;

public class Sourse {
    private final String title;
    private final int inage;


    public Sourse(String title, int inage) {
        this.title = title;
        this.inage = inage;
    }

    public String getTitle() {
        return title;
    }

    public int getInage() {
        return inage;
    }
}
